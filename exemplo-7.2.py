import numpy as np

arr1 = np.array([1, 2, 3])
arr2 = np.array([4, 5, 6])

# Adição
result = arr1 + arr2
print(result)

# Multiplicação por escalar
result = 2 * arr1
print(result)

# Calcular seno de numeros em um array
result = np.sin(arr1)
print(result)

# Somatorio de valores em um array
result = sum(arr1)
print(result)

