import numpy as np

# Função de exponenciação: np.exp(array)
arr = np.array([1, 2, 3, 4, 5])
result = np.exp(arr)
print(result)

# Função de Seno: np.sin()
arr = np.array([0, np.pi/2, np.pi])
result = np.sin(arr)
print(result)

# Função de Cosseno: np.cos()
arr = np.array([0, np.pi/2, np.pi])
result = np.cos(arr)
print(result)

# Função de Raiz Quadrada: np.sqrt()
arr = np.array([4, 9, 16, 2, 0])
result = np.sqrt(arr)
print(result)

# Função de logaritmo: np.log()
arr = np.array([1, np.e, np.e**2])
result = np.log(arr)
print(result)

# Função de soma: np.sum()
arr = np.array([1, 2, 3, 4, 5])
result = np.sum(arr)
print(result)

# Função de elemento máximo: np.max()
arr = np.array([1, 2, 5, 4, 3])
result = np.max(arr)
print(result)

# Função de elemento mínimo: np.min()
arr = np.array([2, 5, 4, 1, 3])
result = np.min(arr)
print(result)
